package com.icss.mvp.entity;

public class CiCCTInfo {
 
	private int id;
	
	private String name;
	
	private double coefficient;
	
	private double size;
	
	private double equivalent;
	
	private double nBNC;
	
	private double added;
	
	private double modified;
	
	private double deleted;
	
	private double comments_ratio;
	
	private double sort;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public double getCoefficient() {
		return coefficient;
	}

	public void setCoefficient(double coefficient) {
		this.coefficient = coefficient;
	}

	public double getSize() {
		return size;
	}

	public void setSize(double size) {
		this.size = size;
	}

	public double getEquivalent() {
		return equivalent;
	}

	public void setEquivalent(double equivalent) {
		this.equivalent = equivalent;
	}

	public double getnBNC() {
		return nBNC;
	}

	public void setnBNC(double nBNC) {
		this.nBNC = nBNC;
	}

	public double getAdded() {
		return added;
	}

	public void setAdded(double added) {
		this.added = added;
	}

	public double getModified() {
		return modified;
	}

	public void setModified(double modified) {
		this.modified = modified;
	}

	public double getDeleted() {
		return deleted;
	}

	public void setDeleted(double deleted) {
		this.deleted = deleted;
	}

	public double getComments_ratio() {
		return comments_ratio;
	}

	public void setComments_ratio(double comments_ratio) {
		this.comments_ratio = comments_ratio;
	}

	public double getSort() {
		return sort;
	}

	public void setSort(double sort) {
		this.sort = sort;
	}	
	
}
