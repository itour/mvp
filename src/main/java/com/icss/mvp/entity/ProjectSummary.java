package com.icss.mvp.entity;

public class ProjectSummary
{
	private String name;
	private int addNum;
	private int overNum;
	private int fpNum;
	private int tmNum;
	public String getName()
	{
		return name;
	}
	public void setProjName(String name)
	{
		this.name = name;
	}
	public int getAddNum()
	{
		return addNum;
	}
	public void setAddNum(int addNum)
	{
		this.addNum = addNum;
	}
	public int getOverNum()
	{
		return overNum;
	}
	public void setOverNum(int overNum)
	{
		this.overNum = overNum;
	}
	public int getFpNum()
	{
		return fpNum;
	}
	public void setFpNum(int fpNum)
	{
		this.fpNum = fpNum;
	}
	public int getTmNum()
	{
		return tmNum;
	}
	public void setTmNum(int tmNum)
	{
		this.tmNum = tmNum;
	}
	
	
}
